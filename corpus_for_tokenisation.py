                try:
                    data = json.loads(line.decode("utf-8"))  # Decode and parse JSON
                    if isinstance(data, dict) and "text" in data:
                        yield data["text"]
                except json.JSONDecodeError:
                    print(f"Skipping malformed JSON in {file_path}")

