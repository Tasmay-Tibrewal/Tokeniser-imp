def longest_same_char_sequence(s: str) -> int:
    if not s:
        return 0

    max_length = 1
    current_length = 1

    for i in range(1, len(s)):
        if s[i] == s[i - 1]:
            current_length += 1
        else:
            max_length = max(max_length, current_length)
            current_length = 1

    return max(max_length, current_length)

